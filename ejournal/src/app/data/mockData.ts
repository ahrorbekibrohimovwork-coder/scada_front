const now = new Date();
const iso = (d: Date) => d.toISOString();
const dAgo = (n: number) => iso(new Date(now.getTime() - n * 86400000));
const dLater = (n: number) => iso(new Date(now.getTime() + n * 86400000));

const sig = (userId: string, n: number = 0): import('../types').EDSSignature => {
  const u = MOCK_USERS.find(u => u.id === userId)!;
  return {
    userId,
    userName: u.name,
    userPosition: u.position,
    userGroup: u.electricalGroup,
    timestamp: dAgo(n),
  };
};

